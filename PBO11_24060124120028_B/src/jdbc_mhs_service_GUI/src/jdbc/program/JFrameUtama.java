    private void jEditButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditButton1ActionPerformed
        // TODO add your handling code here:
        if(jIDEditTextField1.getText().isEmpty() || jNamaEditTextField1.getText().isEmpty()){ return; }
        int id = Integer.parseInt(jIDEditTextField1.getText());
        String nama = jNamaEditTextField1.getText();
        service.update(new Mahasiswa(id, nama));
        jAmbilSemuaDataButton.doClick();
        
        jIDEditTextField1.setText(""); jNamaEditTextField1.setText("");
        jIDHapusTextField.setText(""); jNamaHapusTextField.setText("");
    }//GEN-LAST:event_jEditButton1ActionPerformed

