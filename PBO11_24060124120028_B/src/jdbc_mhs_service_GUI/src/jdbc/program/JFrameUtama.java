    private void jAmbilSemuaDataButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAmbilSemuaDataButtonActionPerformed
        // TODO add your handling code here:
        DefaultListModel<String> idModel = new DefaultListModel<>();
        DefaultListModel<String> nameModel = new DefaultListModel<>();
    
        List<Mahasiswa> list = service.getAll();
        for (Mahasiswa mhs : list) {
            idModel.addElement(String.valueOf(mhs.getId()));
            nameModel.addElement(mhs.getNama());
        }
        jList1.setModel(idModel);
        jList2.setModel(nameModel);
    }//GEN-LAST:event_jAmbilSemuaDataButtonActionPerformed

    private void jTambahkanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTambahkanButtonActionPerformed
        // TODO add your handling code here:
        String nama = jNamaTambahkanTextField1.getText();
        if(nama.trim().isEmpty()) return;
        
        // Menggunakan pencarian ID maksimal manual agar klop dengan database non-auto increment
        int nextId = 1;
        List<Mahasiswa> list = service.getAll();
        for (Mahasiswa mhs : list) {
            if (mhs.getId() >= nextId) {
                nextId = mhs.getId() + 1;
            }
        }
        
        service.add(new Mahasiswa(nextId, nama));
        jAmbilSemuaDataButton.doClick();
        jNamaTambahkanTextField1.setText("");
    }//GEN-LAST:event_jTambahkanButtonActionPerformed

